import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.apache.hadoop.hive.ql.exec.Description;
import org.apache.hadoop.hive.ql.exec.UDFArgumentException;
import org.apache.hadoop.hive.ql.exec.UDFArgumentLengthException;
import org.apache.hadoop.hive.ql.metadata.HiveException;
import org.apache.hadoop.hive.ql.udf.generic.GenericUDF;
import org.apache.hadoop.hive.serde2.objectinspector.ObjectInspector;
import org.apache.hadoop.hive.serde2.objectinspector.ObjectInspectorConverters;
import org.apache.hadoop.hive.serde2.objectinspector.PrimitiveObjectInspector;
import org.apache.hadoop.hive.serde2.objectinspector.primitive.PrimitiveObjectInspectorFactory;
import org.apache.hadoop.io.Text;

import java.text.ParseException;
import java.text.SimpleDateFormat;
import java.util.Calendar;
import java.util.Date;

/**
 * GenericUDFAddMonths.
 *
 * Add a number of months to the date. The time part of the string will be
 * ignored.
 *
 */
@Description(name = "add_months", value = "_FUNC_(start_date, num_months) - Returns the date that is num_months after start_date.", extended = "start_date is a string in the format "
		+ " 'yyyyMMdd'. num_months is a number. The time part of start_date is "
		+ "ignored.\n"
		+ "Example:\n "
		+ " > SELECT _FUNC_('20090831', 1) FROM src LIMIT 1;\n"
		+ " '20090930'\n")
public class UDFAddMonths extends GenericUDF {
	private final Calendar calendar = Calendar.getInstance();
	private final Text output = new Text();
	private static final SimpleDateFormat standedFormatter = new SimpleDateFormat("yyyyMMdd");
	//用于将输入类型转换为目标类型
	private ObjectInspectorConverters.Converter[] converters;

	@Override
	public ObjectInspector initialize(ObjectInspector[] arguments)
			throws UDFArgumentException {
		if (arguments.length != 2) {
			throw new UDFArgumentLengthException("The function COUNTRY(ip, geolocfile) takes exactly 2 arguments.");
		}
		converters = new ObjectInspectorConverters.Converter[arguments.length];
		PrimitiveObjectInspector primitiveOI = (PrimitiveObjectInspector) arguments[0];
		ObjectInspector stringOI = PrimitiveObjectInspectorFactory.writableIntObjectInspector;
		//创建一个Converter，可以在evaluate方法中使用它 将参数的原生数据类型转存为Java Strings
		converters[0] = ObjectInspectorConverters.getConverter(primitiveOI, stringOI);

		primitiveOI = (PrimitiveObjectInspector) arguments[1];
		stringOI = PrimitiveObjectInspectorFactory.writableStringObjectInspector;
		converters[1] = ObjectInspectorConverters.getConverter(primitiveOI, stringOI);
		// 指定UDF （evaluate函数）返回类型为String
		return PrimitiveObjectInspectorFactory.writableStringObjectInspector;
	}

	public static void main(String[] args) {
	}

	@Override
	public Text evaluate(DeferredObject[] arguments) throws HiveException {
		int numMonths;
		try {
			Object arg1;
			Object arg0;
			if ((arg1 = arguments[1].get()) == null || (arg0 = arguments[0].get()) == null) {
				return null;
			}
			Object writableValue = converters[1].convert(arg1);
			numMonths = Integer.valueOf((writableValue).toString());
			Date inputDate;
			try {
				inputDate = standedFormatter.parse(converters[0].convert(arg0).toString());
			} catch (ParseException e) {
				return null;
			}
			calendar.setTime(inputDate);
			calendar.add(Calendar.MONTH, numMonths);
			int maxDd = calendar.getActualMaximum(Calendar.DAY_OF_MONTH);
			if (maxDd < calendar.get(Calendar.DAY_OF_MONTH)) {
				calendar.set(Calendar.DAY_OF_MONTH, maxDd);
			}
			output.set(standedFormatter.format(calendar.getTime()));
		} catch (Exception e) {
			e.printStackTrace();
			return null;
		}
		return output;
	}

	//在出现异常时，会调用这个方法，输出调用信息
	@Override
	public String getDisplayString(String[] children) {
		StringBuilder sb = new StringBuilder();
		sb.append("add_months");
		sb.append("(");
		if (children.length > 0) {
			sb.append(children[0]);
			for (int i = 1; i < children.length; i++) {
				sb.append(",");
				sb.append(children[i]);
			}
		}
		sb.append(")");
		return sb.toString();
	}
}
